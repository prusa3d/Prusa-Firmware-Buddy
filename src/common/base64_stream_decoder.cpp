int Base64StreamDecoder::ConsumeChar(char c, uint8_t *out) {
    // pozor na konec zaznamu - special handling
    uint8_t ic = (c != '=') ? find64(static_cast<uint8_t>(c)) : 0;
    if (ic == 0xff) {
        return -1; // neplatny vstupni znak
    }

    switch (state) {
    case States::AwaitingFirst:
        lastbits = ic;
        state = States::FirstByteOut;
        return 0;
    case States::FirstByteOut: {
        *out = (lastbits << 2) | (ic >> 4);
        lastbits = ic;
        state = States::SecondByteOut;
    }
        return 1;
    case States::SecondByteOut: {
        *out = (lastbits << 4) | (ic >> 2);
        lastbits = ic; // zbyvaji mi 2 bity na pristi kus
        state = States::ThirdByteOut;
    }
        return 1;
    case States::ThirdByteOut: {
        *out = (lastbits << 6) | ic;
        lastbits = 0; // nic nezbylo
        state = States::AwaitingFirst;
    }
        return 1;
    }
    return -1; // just in case...
}
